CREATE TABLE users
(
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email         VARCHAR,
    first_name    VARCHAR,
    last_name     VARCHAR,
    phone         VARCHAR,
    reg_date      VARCHAR,
    city          VARCHAR,
    password      VARCHAR,
    image         VARCHAR
);

